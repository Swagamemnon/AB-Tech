# alien colors with 'if' block
alien_colors = 'yellow'
if alien_colors == 'green':
    print('Congratulations, 5 points!!!')
if alien_colors != 'green':
    print('Congratulations, 10 points!!!')

# alien colors with 'else' block
alien_colors = 'yellow'
if alien_colors == 'green':
    print('Congratulations, 5 points!!!')
else:
    print('Congratulations, 10 points!!!')